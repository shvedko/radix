func BenchmarkRadix_100(b *testing.B) {
	t := radix.New[int]()

	for i := 0; i < 100; i++ {
		s := strconv.Itoa(i)
		c := strconv.Itoa(i % 8)
		t.Insert(i, false, []byte("City"+c), []byte("Street"+s))
	}

	d := func([]byte, int, bool, []int) bool { return true }

	b.ResetTimer()

	b.Run("Dump", func(b *testing.B) {
		b.ResetTimer()
		for i := 0; i < b.N; i++ {
			t.Dump(d)
		}
	})

	b.Run("Walk", func(b *testing.B) {
		b.ResetTimer()
		for i := 0; i < b.N; i++ {
			t.Walk(d)
		}
	})
}
